void Title(char *ch) {
    for (int i = 0; ch[i] != '\0'; i++) {
        if (ch[i] == '#')
            printf(BOLD_MAGENTA "#" RESET);     
        else if (ch[i] == '\'')
            printf(COLOR_51 "'" RESET);   
        else if (ch[i] == ':')
            printf(COLOR_51 ":" RESET);     
        else if (ch[i] == '.')
            printf(COLOR_51 "." RESET);     
        else
            putchar(ch[i]);             
    }
}

